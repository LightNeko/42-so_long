#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*arr;
	size_t	i;

	arr = malloc((ft_strlen(s) + 1) * sizeof(char));
	if (!arr)
		return (NULL);
	i = 0;
	while (s[i])
	{
		arr[i] = s[i];
		i++;
	}
	arr[i] = '\0';
	return (arr);
}
