#include "libft.h"

int	ft_isprint(int c)
{
	if (ft_isalnum(c) || (c >= 32 && c <= 47) || (c >= 58 && c <= 64) \
		|| (c >= 91 && c <= 96) || (c >= 123 && c <= 126))
		return (1);
	else
		return (0);
}
